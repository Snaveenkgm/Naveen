import java.util.*;;

public class Nestedforloop {
    public static void main(String args[])
    {
        int i;
        int j;

        for(i=1;i<=4;i++)
        {
            System.out.println(i);

            for(j=1;j<=4;j++)
            {
                System.out.print(j);

            }
            System.out.println();

        }

    }
}
