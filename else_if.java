public class else_if {
    public static void main(String[] args) {
        int score=62;
        if(score>35&& score<60){
            System.out.println("Video Game");
        }
        else if(score>60&& score<90){
            System.out.println("Iphone");
        }
        else if(score>90){
            System.out.println("Macbook pro");
        }
    }
}
